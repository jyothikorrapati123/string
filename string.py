#string datatype
#implicit
a= "Jyothi"
print(a)
print(type(a))
#explicit
a = str("10")
print(a)
print(type(a))
#datatype/variable annotation
a : str = "Jyothi"
print(a)
print(type(a))
#string conversions
#string to binary
a = bin(10)
print(a)
print(type(a))
# binary to string
a = str (0b1010000000000000000000000011111111111111)
print(a)
print(type(a))
#octal to string
a = str(0o1234522222222)
print(a)
print(type(a))
#string to octal
a = oct(11111111111111111111111)
print(a)
print(type(a))
#string to hexa
a = hex(123456789)
print(a)
print(type(a))
#hexa to string
a = str(0x123456aaaabbbbbbcccccc)
print(a)
print(type(a))
#decimal
#string to integer
a = int("123")
print(a)
print(type(a))
#integer to string
a = str(1234445657757)
print(a)
print(type(a))
#string to float
a =float("1234456")
print(a)
print(type(a))
#float to string
a = str(10.10000)
print(a)
print(type(a))
#exponential to string
a = str(1e2222222222222)
print(a)
print(type(a))
#string to complex
a =complex ("1233344444")
print(a)
print(type(a))
#complex to string
a = str(12354664774777777+7777777j)
print(a)
print(type(a))
#strin to bool
a = bool("12345566")
print(a)
print(type(a))
#bool to string
a = str(True)
print(a)
print(type(a))
#false
a= bool("0000000")
print(a)
print(type(a))
#false
a = str(False)
print(a)
print(type(a))